                                    Media sebagai ruang publik bertujuan untuk melayani kepentingan publik yang luas dan beragam, dimana public harus memperoleh kebutuhan informasi mereka dari media massa secara berimbang dan proporsional.<br /><br />
                                	Namun, berdasarkan data penelitian Remotivi dan FIKOM Unpad Bandung, menunjukan bahwa Jabodetabek sangat mendominasi siaran pertelevisian di Indonesia. Jabodetabek mendominasi; sumber berita sekitar 55%; dari frekuensi tayang 69%; dan dari segi durasi 73%.<br /><br />
                                	Hal ini menimbulkan berbagai macam akibat, misalnya banyak daerah potensial Indonesia yang tidak mendapatkan peliputan media yang cukup. Lalu adanya arus terpaan gaya hidup metropolitan yang turut diterima masyarakat daerah lainnya yang tidak sesuai dengan kultur daerah masing-masing.<br /><br />
