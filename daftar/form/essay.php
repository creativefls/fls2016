                                    Indonesia merupakan salah satu negara yang mempunyai potensi dalam pengembangan industri kreatif baik di kawasan ASEAN maupun pasar dunia. Karena, tidak terlepas dari potensi bahan baku di Indonesia yang melimpah. Hal ini selaras dengan Rencana Pembangunan Nasional Indonesia pada tahun 2005-2025, yang dituangkan dalam Rencana Pembangunan Jangka Menengah (RPJM).<br /><br /> 
                                    Setiap tahapan memiliki skala prioritas dan strategi pembangunan yang merupakan kesinambungan dari periode sebelumnya. RPJM-1 (2005-2009) diarahkan untuk meningkatkan kesadaran masyarakan akan pentingnya kreativitas dan ekonomi kreatif.  RPJM-3 (2015-2019) merupakan upaya untuk meningkatkan daya saing ekonomi kreatif secara berkelanjutan di tingkat nasional dan global.<br /><br />
                                    Pertumbuhan ekonomi kreatif Indonesia mencapai 5,76 persen atau lebih tinggi dari pertumbuhan ekonomi nasional yang sebesar 5,74 persen. Akan tetapi, ekonomi kreatif masih memiliki berbagai kendala salah satunya iklim kondusif untuk mendukung berkembangnya industri kreatif.<br /><br />
                                    Bagaimana membangun iklim kreatif sehingga mendorong masyarakat untuk membangun usaha? Terlebih lagi masyarakat Ekonomi Asean (MEA) menjadi momok nyata yang harus dihadapi.
