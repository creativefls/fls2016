                                    Ancaman perubahan iklim akibat pemanasan global menjadi salah satu akibat keras kepalanya manusia. Padahal pemanasan global telah menjadi isu internasional, namun penghancuran lingkungan khususnya di Indonesia terus terjadi. Berbagai perusakan alam terjadi dimana mana, dari mulai penebangan hutan tropis, perambahan hutan mangrove, hingga penumpukan sampah yang tidak terkendali.<br /><br />
                                	Seperti yang terjadi di Semenanjung Kampar, Riau (Sumatra). Semenanjung tersebut merupakan wadah dari lahan gambut tropis terbesar di dunia, berlokasi di pantai timur Riau dan meliputi area lebih dari 700.000 hektar yang menyimpan banyak cadangan karbon dunia. Hal ini menjadikan ekosistem lahan gambut sebagai kunci pertahanan menghadapi perubahan iklim. Namun saat ini, kondisi semenanjung tersebut sudah sangat kritis. Disamping itu juga maraknya perambahan hutan mangrove dan perusakan ekosistem pesisir terus berlanjut, sementara upaya rehabilitasi dan penanaman kembali yang dilakukan berjalan sangat lambat, bahkan hampir tidak ada.<br /><br /> 
                                    Bagaimana pendapatmu mengenai perambahan hutan mangrove tersebut? Upaya apa yang dapat dilakukan untuk mengurangi ancaman perubahan iklim yang terjadi di Indonesia?
